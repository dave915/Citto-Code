export type TranslationKey = keyof typeof TRANSLATIONS.ko

export function translate(language: AppLanguage, key: TranslationKey, params: TranslationParams = {}) {
  const entry = TRANSLATIONS[language][key] ?? TRANSLATIONS.ko[key]
  return typeof entry === 'function' ? entry(params) : entry
}

export function getIntlLocale(language: AppLanguage): 'ko-KR' | 'en-US' {
  return language === 'en' ? 'en-US' : 'ko-KR'
}

export function pickLocalized<T>(language: AppLanguage, values: { ko: T; en: T }): T {
  return values[language]
}
